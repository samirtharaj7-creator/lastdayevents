function summarizeSupportingSource(source) {
  const text = String(source);
  const isSequenceOnly = /\bThe End of Time\b/i.test(text) && !/\bHayward\b/i.test(text);
  const details = text
    .replace(/\bHayward chart items?:?\s*/i, "")
    .replace(/\bHayward chronology items?:?\s*/i, "")
    .replace(/\bThe Time of the End chart index\b/i, "historical signs index")
    .replace(/\bThe End of Time,\s*/i, "")
    .replace(/\bLayward\b/gi, "")
    .replace(/\bVance\b/gi, "")
    .replace(/\bHayward\b/gi, "")
    .replace(/\bchapter(s)?\s*/gi, "section ")
    .replace(/\b1888 General Conference Session,\s*/i, "")
    .replace(/\s+/g, " ")
    .replace(/^[:,\s-]+|[:,\s-]+$/g, "");

  if (/historical signs index/i.test(details)) {
    return "The historical signs prepare the way for the time-of-the-end message.";
  }

  if (isSequenceOnly || /^section\b/i.test(details)) {
    return "This stage adds sequence detail to the larger last-day chronology.";
  }

  const topic = details.replace(/\bRighteousness by Faith Revived\b/g, "righteousness by faith revived");
  return `Key emphasis: ${topic.charAt(0).toLowerCase()}${topic.slice(1)}.`;
}

